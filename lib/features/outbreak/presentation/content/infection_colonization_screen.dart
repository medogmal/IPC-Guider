            icon: Icons.local_hospital_outlined,
          ),

          const SizedBox(height: 16),

          // Colonization Definition
          _buildDefinitionCard(
            title: 'Colonization',
            definition: 'The presence of microorganisms on or in a host, without tissue invasion or clinical symptoms.',
            example: 'Nasal MRSA detected on screening swab without infection.',
